package com.mycompany.homework4;

/**
 *
 * @author omerfaruk
 */
public class Campaign {
    
    private String campaignName;
    private double campaignAmonut;
    private String campaignDuration;
    
    /**
     * @return the campaignName
     */
    public String getCampaignName() {
        return campaignName;
    }

    /**
     * @param campaignName the campaignName to set
     */
    public void setCampaignName(String campaignName) {
        this.campaignName = campaignName;
    }

    /**
     * @return the campaignAmonut
     */
    public double getCampaignAmonut() {
        return campaignAmonut;
    }

    /**
     * @param campaignAmonut the campaignAmonut to set
     */
    public void setCampaignAmonut(double campaignAmonut) {
        this.campaignAmonut = campaignAmonut;
    }

    /**
     * @return the campaignDuration
     */
    public String getCampaignDuration() {
        return campaignDuration;
    }

    /**
     * @param campaignDuration the campaignDuration to set
     */
    public void setCampaignDuration(String campaignDuration) {
        this.campaignDuration = campaignDuration;
    }
    
  
}
